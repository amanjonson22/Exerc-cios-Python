# Adição
print('Adição: 15 + 20 =', 15 + 20)

# Subtração
print('Subtração: 12 - 2 =', 12 - 2)

# Multiplicação
print('Multiplicação: 22 * 3 =', 22 * 3)

# Divisão
print('Divisão: 25/5 =', 25/5)

# Divisão inteira
print('Divisão inteira: 25//5 =', 25//5)

# Módulo (ou resto de divisão)
print('O resto da divisão: 25%2 =', 25%2)

# Exponenciação
print('Exponencial: 2 ** 3 =', 2**3)