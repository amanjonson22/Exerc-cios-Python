saldo = 450
saque = 200

print(f'saldo: {saldo}\nsaque: {saque}\n')

print('comparadores maior/menor e maior igual a/menor igual a\n')
print(f'saldo({saldo}) > saque({saque}):', saldo > saque)
print(f'saldo({saldo}) >= saque({saque}):', saldo >= saque)
print(f'saldo({saldo}) < saque({saque}):', saldo < saque)
print(f'saldo({saldo}) <= saque({saque}):', saldo <= saque, end='\n\n')

print(f'comparadores de diferente de/igual a\n')
print(f'saldo({saldo}) != saque({saque}):',saldo != saque)
print(f'saldo({saldo}) == saque({saque}):',saldo == saque, end='\n\n')