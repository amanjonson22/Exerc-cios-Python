#Atribuindo valores

#O sinal de igual atribui valor
saldo = 500
print(saldo)

#O sinal de += ou -= atribui um novo valor somando/subtraindo o antigo ao novo
saldo += 200
print(saldo)

saldo -= 150
print(saldo)

#outros sinais de atribuição
saldo = 100
saldo *= 22
print(saldo)

saldo = 150
saldo /= 5
print(saldo)

saldo = 200
saldo //= 20
print(saldo)

saldo = 80
saldo **= 3
print(saldo)

saldo = 2000
saldo %= 30
print(saldo)